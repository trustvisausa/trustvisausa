// ===== LANGUAGE SWITCHER =====
let currentLang = localStorage.getItem('tvusa-lang') || 'es';

function setLang(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key] !== undefined) el.innerHTML = translations[lang][key];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (translations[lang][key] !== undefined) el.placeholder = translations[lang][key];
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang;
  localStorage.setItem('tvusa-lang', lang);
  currentLang = lang;
}

document.addEventListener('DOMContentLoaded', () => {
  // Init language
  setLang(currentLang);

  // Lang button clicks
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });

  // ===== NAVBAR SCROLL =====
  const navbar = document.getElementById('navbar');
  window.addEventListener('scroll', () => {
    navbar.classList.toggle('scrolled', window.scrollY > 60);
  });

  // ===== MOBILE MENU =====
  const hamburger = document.getElementById('hamburger');
  const navLinks = document.getElementById('navLinks');
  hamburger.addEventListener('click', () => {
    hamburger.classList.toggle('open');
    navLinks.classList.toggle('open');
    document.body.style.overflow = navLinks.classList.contains('open') ? 'hidden' : '';
  });
  navLinks.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', () => {
      hamburger.classList.remove('open');
      navLinks.classList.remove('open');
      document.body.style.overflow = '';
    });
  });

  // ===== CONTACT FORM — Web3Forms =====
  const form = document.getElementById('contactForm');
  if (form) {
    form.addEventListener('submit', async (e) => {
      e.preventDefault();
      const btn  = document.getElementById('formSubmitBtn');
      const ok   = document.getElementById('formSuccess');
      const err  = document.getElementById('formError');
      ok.style.display = 'none';
      err.style.display = 'none';
      btn.disabled = true;
      btn.innerHTML = '⏳ Enviando…';

      try {
        const res  = await fetch('https://api.web3forms.com/submit', {
          method: 'POST',
          body: new FormData(form)
        });
        const data = await res.json();
        if (data.success) {
          ok.style.display = 'block';
          form.reset();
        } else {
          err.style.display = 'block';
        }
      } catch (_) {
        err.style.display = 'block';
      } finally {
        btn.disabled = false;
        btn.innerHTML = translations[currentLang]['form.btn'] || 'Enviar Mensaje →';
      }
    });
  }

  // ===== SCROLL REVEAL =====
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) entry.target.classList.add('visible');
    });
  }, { threshold: 0.1 });

  document.querySelectorAll('.service-card, .step, .testimonial-card, .why-feature').forEach(el => {
    el.classList.add('reveal');
    observer.observe(el);
  });
});
